#include "ft_printf.h"

int	ft_di(int n)
{
	int long	nbr;
	int			i;

	nbr = n;
	i = 0;
	if (nbr < 0)
	{
		nbr *= -1;
		i += ft_c('-');
	}
	if (nbr >= 10)
		i += ft_di(nbr / 10);
	i += ft_c(nbr % 10 + 48);
	return (i);
}
